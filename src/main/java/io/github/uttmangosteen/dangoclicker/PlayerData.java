package io.github.uttmangosteen.dangoclicker;

public class PlayerData {
    public double stock;
    public double DPC;
    public double DPS;
    public int cursorAmount;
    public double cursorPrise;
    public int grandmaAmount;
    public double grandmaPrise;
    public int factoryAmount;
    public double factoryPrise;
    public int mineAmount;
    public double minePrise;
    public int shipmentAmount;
    public double shipmentPrise;
    public int alchemyLabAmount;
    public double alchemyLabPrise;
    public int portalAmount;
    public double portalPrise;
    public int timeMachineAmount;
    public double timeMachinePrise;

    public PlayerData(double stock, double dpc, double dps, int cursorAmount, double cursorPrise, int grandmaAmount, double grandmaPrise, int factoryAmount, double factoryPrise, int mineAmount, double minePrise, int shipmentAmount, double shipmentPrise, int alchemyLabAmount, double alchemyLabPrise, int portalAmount, double portalPrise, int timeMachineAmount, double timeMachinePrise){

        this.stock = stock;
        this.DPC = dpc;
        this.DPS = dps;
        this.cursorAmount = cursorAmount;
        this.cursorPrise = cursorPrise;
        this.grandmaAmount = grandmaAmount;
        this.grandmaPrise = grandmaPrise;
        this.factoryAmount = factoryAmount;
        this.factoryPrise = factoryPrise;
        this.mineAmount = mineAmount;
        this.minePrise = minePrise;
        this.shipmentAmount = shipmentAmount;
        this.shipmentPrise = shipmentPrise;
        this.alchemyLabAmount = alchemyLabAmount;
        this.alchemyLabPrise = alchemyLabPrise;
        this.portalAmount = portalAmount;
        this.portalPrise = portalPrise;
        this.timeMachineAmount = timeMachineAmount;
        this.timeMachinePrise = timeMachinePrise;
    }
}
